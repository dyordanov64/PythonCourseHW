#  --------------- Въвеждаме цяло чистло от клавиатурата -------------
integer_number = int( input("Въведете цяло число="))


if integer_number%2: # проверяваме остатъка от целочисленото деление дали е True или False
    print("Въведеното цяло число {} е нечетно".format(integer_number)) # ако остатъка е True числото е нечетно
else:
    print("Въведеното цяло число {} е четно".format(integer_number)) # ако остатъка е False числото е четно

